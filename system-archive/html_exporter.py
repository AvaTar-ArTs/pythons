    def _generate_category_chart(self, categories: Dict) -> str:
        """Generate HTML for category chart."""
        if not categories:
            return "<p>No category data available</p>"
        
        # Create a simple bar chart
        max_count = max(categories.values())
        chart_html = "<div style='display: flex; flex-direction: column; gap: 10px;'>"
        
        for category, count in sorted(categories.items(), key=lambda x: x[1], reverse=True)[:10]:
            width = (count / max_count) * 100
            chart_html += f"""
            <div style='display: flex; align-items: center; gap: 10px;'>
                <div style='min-width: 100px; font-size: 0.9em;'>{category}</div>
                <div style='flex: 1; background: #f0f0f0; height: 20px; border-radius: 10px; overflow: hidden;'>
                    <div style='background: #4CAF50; height: 100%; width: {width}%; transition: width 0.3s ease;'></div>
                </div>
                <div style='min-width: 50px; text-align: right; font-weight: bold;'>{count:,}</div>
            </div>
            """
        
        chart_html += "</div>"
        return chart_html
    
    def _generate_depth_chart(self, depth_distribution: Dict) -> str:
        """Generate HTML for depth chart."""
        if not depth_distribution:
            return "<p>No depth data available</p>"
        
        max_count = max(depth_distribution.values())
        chart_html = "<div style='display: flex; align-items: end; height: 200px; gap: 5px;'>"
        
        for depth in sorted(depth_distribution.keys()):
            count = depth_distribution[depth]
            height = (count / max_count) * 180
            chart_html += f"""
            <div style='display: flex; flex-direction: column; align-items: center;'>
                <div style='background: #3498db; width: 30px; height: {height}px; border-radius: 4px 4px 0 0;'></div>
                <div style='margin-top: 5px; font-size: 0.8em; font-weight: bold;'>{depth}</div>
                <div style='font-size: 0.7em; color: #666;'>{count}</div>
            </div>
            """
        
        chart_html += "</div>"
        return chart_html
    
    def _generate_file_types_list(self, file_types: Dict) -> str:
        """Generate HTML for file types list."""
        if not file_types:
            return "<p>No file type data available</p>"
        
        html = ""
        for ext, count in sorted(file_types.items(), key=lambda x: x[1], reverse=True)[:15]:
            html += f"""
            <div class="list-item">
                <span>.{ext}</span>
                <span>{count:,}</span>
            </div>
            """
        return html
    
    def _generate_size_by_category_list(self, size_by_category: Dict) -> str:
        """Generate HTML for size by category list."""
        if not size_by_category:
            return "<p>No size data available</p>"
        
        total_size = sum(size_by_category.values())
        html = ""
        for category, size in sorted(size_by_category.items(), key=lambda x: x[1], reverse=True)[:10]:
            size_mb = round(size / (1024 * 1024), 2)
            percentage = round((size / total_size) * 100, 1) if total_size > 0 else 0
            html += f"""
            <div class="list-item">
                <span>{category}</span>
                <span>{size_mb} MB ({percentage}%)</span>
            </div>
            """
        return html
    
    def _generate_file_table_rows(self, file_data: List[Dict]) -> str:
        """Generate HTML table rows for file data."""
        if not file_data:
            return "<tr><td colspan='8'>No file data available</td></tr>"
        
        html = ""
        for file_info in file_data[:100]:  # Limit to first 100 files
            duplicate_class = "duplicate" if file_info.get('is_duplicate', False) else ""
            html += f"""
            <tr class="{duplicate_class}">
                <td>{file_info.get('name', '')}</td>
                <td style='max-width: 300px; overflow: hidden; text-overflow: ellipsis;'>{file_info.get('path', '')}</td>
                <td>.{file_info.get('extension', '')}</td>
                <td class="size-cell">{file_info.get('size_mb', 0)}</td>
                <td>{file_info.get('category', '')}</td>
                <td>{file_info.get('depth', 0)}</td>
                <td>{file_info.get('modified_time', '')[:10]}</td>
                <td>{'Yes' if file_info.get('is_duplicate', False) else 'No'}</td>
            </tr>
            """
        return html
    
    def _generate_category_cards(self, categories: Dict, size_by_category: Dict, total_files: int) -> str:
        """Generate HTML for category cards."""
        if not categories:
            return "<p>No category data available</p>"
        
        html = ""
