        print("   🌐 Exporting HTML files...")
        
        # Export main dashboard
        self._export_dashboard(result, output_dir)
        
        # Export detailed analysis pages
        self._export_file_analysis_page(result, output_dir)
        self._export_category_analysis_page(result, output_dir)
        self._export_duplicate_analysis_page(result, output_dir)
