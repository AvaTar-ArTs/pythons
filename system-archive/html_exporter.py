        max_count = max(depth_distribution.values())
        html = ""
        
        for depth in sorted(depth_distribution.keys()):
            count = depth_distribution[depth]
            height = (count / max_count) * 250
            html += f"""
            <div class="depth-bar" style="height: {height}px;">
                <div class="depth-value">{count}</div>
                <div class="depth-label">Depth {depth}</div>
            </div>
            """
        
        return html
    
