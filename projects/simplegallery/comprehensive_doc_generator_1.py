        for project_dir in self.base_dir.iterdir():
            if project_dir.is_dir() and not project_dir.name.startswith("."):
                project_name = project_dir.name
                api_index += f"### {project_name}\n\n"

                # Find Python files in project
                python_files = list(project_dir.rglob("*.py"))
                for py_file in python_files:
                    if py_file.name != "__init__.py":
                        module_name = py_file.stem
                        api_index += f"#### {module_name}\n\n"
                        api_index += (
                            f"**File**: `{py_file.relative_to(self.base_dir)}`\n\n"
                        )
                        api_index += f"```python\n# Module: {module_name}\n```\n\n"

        with open(self.api_dir / "index.md", "w", encoding="utf-8") as f:
            f.write(api_index)

