    def _build_sphinx_docs(self):
        """Build Sphinx documentation."""
        logger.info("🔨 Building Sphinx documentation...")

        try:
            # Change to sphinx directory
            os.chdir(self.sphinx_dir)

            # Run sphinx-build
            result = subprocess.run(
                ["sphinx-build", "-b", "html", "source", "_build/html"],
                capture_output=True,
                text=True,
            )

            if result.returncode == 0:
                logger.info("✅ Sphinx documentation built successfully")
            else:
                logger.warning(f"⚠️  Sphinx build warnings: {result.stderr}")

        except FileNotFoundError:
            logger.warning(
                "⚠️  Sphinx not installed. Install with: pip install sphinx sphinx-rtd-theme"
            )
        except Exception as e:
            logger.error(f"❌ Error building Sphinx docs: {e}")
        finally:
            # Change back to original directory
            os.chdir(self.base_dir)

    def _generate_pydoc_docs(self):
        """Generate PyDoc documentation."""
        logger.info("📖 Generating PyDoc documentation...")

        try:
            # Generate PyDoc for each Python file
            for py_file in self.base_dir.rglob("*.py"):
                if py_file.name != "__init__.py":
                    # Generate HTML documentation
                    result = subprocess.run(
                        ["python", "-m", "pydoc", "-w", str(py_file)],
                        capture_output=True,
                        text=True,
                        cwd=self.pydoc_dir,
                    )

                    if result.returncode == 0:
                        # Move generated HTML file
                        html_file = self.pydoc_dir / f"{py_file.stem}.html"
                        if html_file.exists():
                            shutil.move(
                                str(html_file),
                                str(self.pydoc_dir / "html" / f"{py_file.stem}.html"),
                            )

            # Create PyDoc index
            self._create_pydoc_index()

            logger.info("✅ PyDoc documentation generated successfully")

        except Exception as e:
            logger.error(f"❌ Error generating PyDoc: {e}")

